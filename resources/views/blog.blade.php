                    <br>
                    <p>
                        Discrimination, racism, inequality, oppression, exploitation, ignorance, silence -
                        the list will go on. We do not live in a utopian society. To ensure rights and make
                        the world a better place to live, we need to raise our voice, point out issues and
                        fight to achieve the goal. Activism embodies the policies and actions taken in such
                        cases. Activists work for the greater good and strive to represent the people
                        whose voices remain unheard or neglected. From climate change to the Black
                        Lives Matter movement and LGBTQ rights movement, we have seen activists
                        continue their battle against the existing policy, perspective, or action. Their
                        creative strategy aims to change public perception of any issue by making it
                        mainstream, forcing robust conversations, and reframing the standards of what is
                        acceptable and what not.
                    </p>
                    <p>
                        The word activism resembles the practice of taking action to bring a change.
                        Specifically, activism refers to the successive effort to promote, direct or prevent
                        actions towards a political or social change. Activists advocate and practice
                        activism. They cover a broad spectrum of issues, including political, social,
                        economic, and environmental policies and actions. Tracing back, we see a vibrant
                        history of activists around the world. From Malala Yousafzai and Anna Hazare to
                        Mahatma Gandhi, Martin Luther King, Jr., and Nelson Mandela – the list of
                        activists proudly contains some of the most remarkable and persuading names.
                    </p>
                    <p>
                        There is no solid structure for activists. They fight for different issues in different
                        ways around the globe. Sometimes, they raise a global voice to form a stronger
                        movement. In recent years, we have encountered different types of activism.
                        Activists choose their preferable platform and build a creative action plan for their
                        issue. Demonstrations, boycotts, strikes, petitions, online and offline campaigns
                        are some forms of activism. It encourages the cultivation of societal alternatives
                        and assists in reforming the structure. Activism leads towards more inclusion and
                        making the world more equitable.
                    </p>
                    <p>
                        The contribution of activism in society is outstanding. Activism fought for
                        women&#39;s right to vote. We found activists fighting against racial discrimination.
                        They ensured the cancellation of adverse laws in different parts of the world.
                        Activism empowers people to talk about issues and breaks the silence.
                        Superstition and stigmatization hinder natural growth from various aspects as

                        they impose barriers. Activism has a long history of battling against them.
                        Smashing the glass ceiling, now we can dream bigger.
                    </p>

                </div>
                <div class="blog__item__quote">
                    <p>“You may never know what results come of your actions, but if you do nothing, there will be no results.”</p>
                    <span>Mahatma Gandhi</span>
                </div>
                <div class="blog__details__text">
                    <p>
                        Reviewing recent events, we may get a better picture of activism. #MeToo
                        became the voice of survivors of sexual harassment. People saw the darker side of
                        the glamorous world. During Hong Kong&#39;s protest against plans to allow
                        extradition to mainland China, activists drew global attention through their
                        actions. The Black Lives Matter movement started to stand against police brutality
                        and all racially motivated violence. Activists created a worldwide response and
                        support. If we consider activists to become the face of a movement, Malala
                        Yousafzai is an obvious example. For publicly speaking about education for girls,
                        terrorists shot her. Still, she continued to dream as an education activist. Swedish
                        environmental activist Greta Thunberg is internationally known for challenging
                        world leaders to take immediate action for climate change mitigation. In the age
                        of social media, activism reached a new height.
                    </p>
                    <p>
                        Day by day, activism is receiving more audiences and active supporters. The
                        participation of both older and younger people with their issues is leading
                        activism to a different dimension. Activism allows us to learn about our life,
                        society, and history from a better perspective. So, we get to realize who is
                        exploiting and how. Young climate activists grabbed our attention and
                        encouraged us to rethink our approach towards the environment. When we see
                        school students are coming out to strike for climate change, it points out how our
                        existing system failed to assure them about their future. On the other hand, we
                        realize, our next generation is considering the wellbeing of our mother earth. It
                        helps us to be optimistic about the future.
                        As activism serves our crying needs, it has turned into a powerful tool for
                        accelerating development. For better tomorrow, an upward spiral of activism is
