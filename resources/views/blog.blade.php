                    <p>Despite our bias towards certain beauty standards, an interesting study revealed a completely
                        different perspective. People from different races such as African-American, Masai tribal were
                        given to judge pictures of people based on their beauty. It was ensured that they remain unaware
                        of the hypotheses. All of them judged the pictures based on beauty standards which were accepted
                        across all races and cultures. All of them found blemish free and symmetrical faces prettier.
                        But a different result showed up when they were given pictures of people coming from their same
                        race. Interestingly, they were now biased to the people of their own race and they found their
                        facial features pretty. To reinstate the hypotheses, some university students were brought in
                        pairs. Each pair were friends to each other. Some were same-sex friends and some were from the
                        opposite sex. They had their photographs taken and were then asked to privately rate each
                        other’s attractiveness, along with photos of other participants whom they did not know
                        (strangers). Friends were also asked to rate each other on personality traits, including
                        “admirable,” “generous,” “sensitive,” “warm.” and many more. This led to a similar type of
                        discovery. The friends rated each other’s prettiness on the parameter of how likeable as a
                        person they were, not on the basis of any beauty standard. But while rating strangers, they took
                        factors like facial symmetry and spot free skin as an important factor.</p>
                    <p>From this study, one idea is very clear. We are biased to people’s inner qualities as much as we
                        are biased to the external ones. And the moment we start realizing those internal traits of a
                        person, we no longer care about how they look. They are beautiful to us simply because they are
                        beautiful people. And this is why please believe yourself when your friends tell you that you
                        are beautiful. And ask no more questions.</p>
