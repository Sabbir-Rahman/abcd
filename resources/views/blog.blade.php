                    <br>
                    <p>
                        Repetition, one of the essential traits of human life. By doing work repeatedly, we get used to
                        it. Similarly, if you repeat something, people are more likely to believe it, even if it is a
                        rumour.
                    </p>
                    <p>
                        Human beings trust their senses. They believe what they see and hear. After verifying an
                        information, they classify it as truth. But if a false information is provided to him
                        repeatedly, he deems it to be true. It is sometimes called “Illusory Truth Effect”.
                    </p>
                    <p>
                        You may think that people who have higher intelligence or knowledge or control of emotion can
                        overcome this effect, but sadly it is not true. This effect is not changed by cognitive
                        ability.But it is not concrete that whatever you hear repeatedly will have permanent effects on
                        you. If anyone tells a person who has just heard the statement that it is false, the effect is
                        lessened. Again, if it is said too much, people will think that it is for persuading
                        them.Lastly, information that is totally faulty cannot affect a person's judgement. For example,
                        “Honey never spoils.”
                    </p>
                    <p>
                        But the question is why does it work. We have false information all around us in advertisements,
                        propaganda websites, even small lies heard from a person. We live in a world where information
                        is the key to hold power. But that information is being misinterpreted in many ways. We are
                        always on our toes to protect us from any kind of false information. But why obvious false
                        information feels to be real after hearing it a couple of times.
                    </p>
                    <p>
                        People accept information after processing it in his mind. He verifies it through various
                        sources and completes his justification. But when he hears the false information repeatedly, his
                        mind starts to process it and thinks of it as information from different sources. Even if he
                        does not think it as concrete information, this false information lingers in his mind and this
                        information affects his decisions in every step.
                    </p>
                </div>
                <div class="blog__item__quote">
                    <p>“The point of modern propaganda isn't only to misinform or push an agenda. It is to exhaust your
                        critical thinking, to annihilate truth.””</p>
                    <span>Garry Kasparov</span>
                </div>
                <div class="blog__details__text">
                    <p>
                        Thus even if a person doesn’t totally believe the information, his processing of a situation
                        becomes defective. This information unifies with his processing, causing a greater incidence.
                        These kinds of incidents increase the encounters of the “Illusory Truth Effect” in the universe.
                    </p>
                    <p>
                        Information shapes our society and culture. People put their beliefs on the system they grew up
                        on. Systems can be easily manipulated by false information. Thus sensitive incidents like voting
                        are being influenced by this “Illusory Truth effect”. Whether a person has set his mind on
                        voting a specific person or not, repeatedly hearing specific information manipulates him from
                        following his own mind. Thus, sensitive issues are being hindered from being processed.
                    </p>
                    <p>
                        Even if all the logics are not valid, we cannot deny the existence of information that collude
                        us from the accurate scenario. Our mind always influences us to believe what we hear and see.
                        Other peoples’ opinions do not affect us that much. But if an information is being repeatedly
                        given to a person on a golden platter, his mind will begin to play tricks on him.
                    </p>
                    <p>
                        Lastly, exposure to a phenomenon always manipulates a person’s judgement. For example, Being
                        constantly exposed to a certain religion, will make him a little easy about that religion’s
                        culture. Thus he starts to become familiar with its customs. Similarly, constantly feeding on
                        information makes him believe in all the incidents that happen for that particular information.
                    </p>
                    <p>
                        Maybe, humankind, as an existence, is not just in all the judgments. But it can be the proper
                        way for that particular time frame. It's not the judgements that affect the time and space, it's
                        the consequences.
                    </p>
