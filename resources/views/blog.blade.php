                        specific set of features can be called beautiful. This set of beauty standards vary from society
                        to society. In the subcontinental culture, the bias towards fair skinned people has become so
                        cliche that now when we talk about it, the bias sounds baseless and somewhat funny. Seems like
                        people have started to finally get out of this bias. But does it mean they have no bias left in
                        them to be inclined to? With the recent surge in pop culture, a new trend of having glass like
                        clear skin has emerged. Having healthy skin is definitely encouraged. But there are some who
                        blindly go at incredible lengths to achieve this target. Failing to achieve the goal, there are
                        many who will eventually start questioning their self-worth. What they forget is the fact that
